    // Tipe data float
    int floatVar = 3.14f;
    std::cout << "Tipe Data Float: " << floatVar << std::endl;
    // Tipe data double
    int doubleVar = 3.14159;
    std::cout << "Tipe Data Double: " << doubleVar << std::endl;
